package com.company;

public class Boss {
    private int bossHealth;
    private int bossDamge;
    private String bossDefence;

    public int getBossHealth() {
        return bossHealth;
    }

    public void setBossHealth(int bossHealth) {
        this.bossHealth = bossHealth;
    }

    public int getBossDamge() {
        return bossDamge;
    }

    public void setBossDamge(int bossDamge) {
        this.bossDamge = bossDamge;
    }

    public String getBossDefence() {
        return bossDefence;
    }

    public void setBossDefence(String bossDefence) {
        this.bossDefence = bossDefence;
    }
}